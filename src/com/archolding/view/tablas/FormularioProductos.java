    private void estadoInicial(Boolean b) {
        tfCodigoBarras.requestFocus();
        btnNuevo.setEnabled(b);
        btnEliminar.setEnabled(b);
        btnGuardar.setEnabled(!b);
        btnLimpiar.setEnabled(!b);
    }

    private void nuevo() {
        accion = "NUEVO";
        estadoInicial(false);
        vaciarCampos();
        jLabel28.setText("INSERTAR");
    }

    private void modificar() {
        accion = "MODIFICAR";
        jLabel28.setText("MODIFICAR");
        estadoInicial(false);
    }

    private void cancelar() {
        estadoInicial(true);
        vaciarCampos();
    }

    private void guardar() {

        if (accion.equals("NUEVO")) {
            producto = new Dproductos();
        }
        producto.setCodbarra(tfCodigoBarras.getText());
        dao = new ProductosDao();

        try {
            if (accion.equals("NUEVO")) {
                dao.insertar(producto);
            } else {
                dao.actualizar(producto);
            }
            dao.commit();
            estadoInicial(true);
            vaciarCampos();
        } catch (Exception e) {
            e.printStackTrace();
            dao.rollback();
            JOptionPane.showMessageDialog(null, "Se produjo un error al guardar los datos");
        }
    }

