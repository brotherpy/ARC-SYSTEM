    private void recuperarTodo() {
        dao = new DepositosDao();
        lista = dao.recuperarTodo();
        mtDdepositos.setLista(lista);
    }

    private void filtrarCadena() {
        dao = new DepositosDao();
        lista = dao.recuperarPorFiltro(tfDescripcion.getText());
        mtDdepositos.setLista(lista);
    }

    private void seleccionarObjeto(int index) {
        if (index < 0) {
            return;
        }

        cadena = lista.get(index);
        tfDescripcion.setText(cadena.getDescripcion());
        modificar();
    }

    private void seleccionarProducto(int index) {
        if (index < 0) {
            return;
        }
        Ddepositos cadena = lista.get(index);
        //interfaz.setProducto(producto);
        dispose();
    }

    private void eliminar() {
        int index = tabla.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
            return;
        }
        cadena = lista.get(index);
        int result = JOptionPane.showConfirmDialog(null,
                "Esta seguro q desea eliminar la cadena "
                + cadena.getDescripcion(),
                "AtenciÃ³n", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            dao = new DepositosDao();
            try {
                dao.eliminar(cadena);
                dao.commit();
                recuperarTodo();
                vaciarCampos();
            } catch (Exception e) {
                dao.rollback();
            }
        }

    }

    //Estado inicial = true (Los campos estan deshabilitados)
    //Estado inicial = false (Los campos estan habilitados)
    private void estadoInicial(Boolean b) {
        tfDescripcion.requestFocus();
        btnNuevo.setEnabled(b);
        btnBuscar.setEnabled(b);
        btnEliminar.setEnabled(b);
        btnGuardar.setEnabled(!b);
        btnCancelar.setEnabled(!b);

        tabla.clearSelection();
        tabla.setEnabled(b);
    }
    //Vaciar campos

    private void vaciarCampos() {
        tfDescripcion.requestFocus();
        tfDescripcion.setText("");
    }

    private void nuevo() {
        accion = "NUEVO";
        estadoInicial(false);
        vaciarCampos();
    }

    private void modificar() {
        accion = "MODIFICAR";
        estadoInicial(false);
    }

    private void cancelar() {
        estadoInicial(true);
        vaciarCampos();
    }

    private void guardar() {

        if (accion.equals("NUEVO")) {
            cadena = new Ddepositos();
        }

        cadena.setDescripcion(tfDescripcion.getText());

        dao = new DepositosDao();

        try {
            if (accion.equals("NUEVO")) {
                dao.insertar(cadena);
            } else {
                dao.actualizar(cadena);
            }
            dao.commit();
            estadoInicial(true);
            vaciarCampos();
            recuperarTodo();
        } catch (Exception e) {
            e.printStackTrace();
            dao.rollback();
            JOptionPane.showMessageDialog(null, "Se produjo un error al guardar los datos");
        }
    }
